INSERT INTO DMBO_ROLE_FUNC (ROLE_ID, FUNCTION_ID, CREATE_USER)
SELECT R.ROLE_ID, F.FUNCTION_ID, 'SYSTEM'
FROM DMBO_ROLE R
JOIN DMBO_FUNCTION F ON UPPER(F.FUNCTION_CODE) IN (
    'ICON_MANAGEMENT', 'ICON_CREATE', 'ICON_UPDATE', 'ICON_DELETE', 'ICON_CHANGE_STATUS', 'ICON_FUNCTION_CODE_VIEW'
)
WHERE UPPER(R.ROLE_CODE) = 'SUPER_ADMIN'
  AND NOT EXISTS (
      SELECT 1 FROM DMBO_ROLE_FUNC RF
      WHERE RF.ROLE_ID = R.ROLE_ID AND RF.FUNCTION_ID = F.FUNCTION_ID
  );

